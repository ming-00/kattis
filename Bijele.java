import java.util.Scanner;

public class Bijele {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
	

		int[] def = {1, 1, 2, 2, 2, 8};

		for(int i = 0; i < def.length; i++) {
			System.out.print(def[i] - sc.nextInt() + " ");
			System.out.println();
		}
		
	sc.close();
	}}


			
